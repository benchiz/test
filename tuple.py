# Кортежи (tuple)
# Если в кортеже находится 1 элемент, то после него необходимо ставить запятую
# Создается с помощью круглых скобок
# Экономнее чем список
# Неизменяемая коллекция данных

person = ('Alex', )
print(person)
person = ('Alex', 'Zoo')
print(person)
# Исп-я в операциях, в которых не должны изменяться объекты